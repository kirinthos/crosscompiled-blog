// Custom PNG emoji mapping for personalized emojis
// Maps emoji names to PNG file paths in /public/images/
export const customEmojiMap: Record<string, string> = {
  'head-scratch': 'head-scratch.gif',
  // Add more custom emojis here as needed
  // 'custom-name': 'filename.png',
};

// Function to convert emoji shortcodes to Unicode or PNG images
export function convertEmojis(text: string): string {
  return text.replace(/:([a-zA-Z0-9-+-]+):/g, (match, emojiName) => {
    const lowerName = emojiName.toLowerCase();
    
    // Check for custom PNG emoji first
    const customEmoji = customEmojiMap[lowerName];
    if (customEmoji) {
      return `<img src="/images/${customEmoji}" alt="${emojiName}" class="inline-emoji" style="display: inline; width: 1.2em; height: 1.2em; vertical-align: text-bottom; margin: 0 0.1em;" />`;
    }
    
    // Fall back to Unicode emoji
    const unicodeEmoji = emojiMap[lowerName];
    return unicodeEmoji || match; // Return original if emoji not found
  });
}

// Function to get all available emoji names (both Unicode and custom PNG)
export function getAvailableEmojis(): string[] {
  const allEmojis = [...Object.keys(emojiMap), ...Object.keys(customEmojiMap)];
  return Array.from(new Set(allEmojis)).sort(); // Remove duplicates and sort
}

// Function to search emojis by name (both Unicode and custom PNG)
export function searchEmojis(query: string): Array<{name: string, emoji: string}> {
  const lowercaseQuery = query.toLowerCase();
  
  // Search Unicode emojis
  const unicodeResults = Object.entries(emojiMap)
    .filter(([name]) => name.includes(lowercaseQuery))
    .map(([name, emoji]) => ({ name, emoji }));
  
  // Search custom PNG emojis
  const customResults = Object.entries(customEmojiMap)
    .filter(([name]) => name.includes(lowercaseQuery))
    .map(([name, filename]) => ({ 
      name, 
      emoji: `<img src="/images/${filename}" alt="${name}" class="inline-emoji" style="display: inline; width: 1.2em; height: 1.2em; vertical-align: text-bottom; margin: 0 0.1em;" />` 
    }));
  
  return [...unicodeResults, ...customResults].slice(0, 20); // Limit results
}
