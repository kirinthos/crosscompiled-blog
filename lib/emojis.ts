// Function to convert emoji shortcodes to Unicode
export function convertEmojis(text: string): string {
  return text.replace(/:([a-zA-Z0-9_+-]+):/g, (match, emojiName) => {
    const emoji = emojiMap[emojiName.toLowerCase()];
    return emoji || match; // Return original if emoji not found
  });
}

// Function to get all available emoji names
export function getAvailableEmojis(): string[] {
  return Object.keys(emojiMap).sort();
}

// Function to search emojis by name
export function searchEmojis(query: string): Array<{name: string, emoji: string}> {
  const lowercaseQuery = query.toLowerCase();
  return Object.entries(emojiMap)
    .filter(([name]) => name.includes(lowercaseQuery))
    .map(([name, emoji]) => ({ name, emoji }))
    .slice(0, 20); // Limit results
}
